/**
 * Export module — handles exporting the SMTP conversation log.
 */

const SmtpExport = {
  /**
   * Export conversation events in the specified format.
   * @param {Array} events - Array of {type, timestamp, data} objects
   * @param {string} host - SMTP host that was tested
   * @param {string} format - "json" | "text" | "transcript"
   */
  export(events, host, format) {
    if (!events || events.length === 0) return;

    let content, mimeType, extension;

    switch (format) {
      case 'json':
        content = this.toJson(events, host);
        mimeType = 'application/json';
        extension = 'json';
        break;
      case 'text':
        content = this.toText(events, host);
        mimeType = 'text/plain';
        extension = 'txt';
        break;
      case 'transcript':
        content = this.toTranscript(events, host);
        mimeType = 'text/plain';
        extension = 'log';
        break;
      default:
        return;
    }

    const timestamp = new Date().toISOString().replace(/[:.]/g, '-').slice(0, 19);
    const safeHost = host.replace(/[^a-zA-Z0-9.-]/g, '_');
    const filename = `smtp-test_${safeHost}_${timestamp}.${extension}`;

    this.download(content, filename, mimeType);
  },

  /**
   * JSON export — full structured data.
   */
  toJson(events, host) {
    const exportData = {
      tool: 'SMTP Tester (https://smtp-tester.tyk.app)',
      host: host,
      exportedAt: new Date().toISOString(),
      events: events.map(e => ({
        type: e.type,
        timestamp: e.timestamp,
        data: e.data,
      })),
    };
    return JSON.stringify(exportData, null, 2);
  },

  /**
   * Plain text export — human-readable log.
   */
  toText(events, host) {
    const lines = [
      '═══════════════════════════════════════════════════',
      '  SMTP Tester — Session Log',
      `  Host: ${host}`,
      `  Date: ${new Date().toISOString()}`,
      '═══════════════════════════════════════════════════',
      '',
    ];

    const prefixMap = {
      sent: 'C: ',
      received: 'S: ',
      info: 'ℹ  ',
      error: '✖  ',
      tls: '🔒 ',
      complete: '✔  ',
    };

    for (const event of events) {
      const time = new Date(event.timestamp).toLocaleTimeString('en-GB', {
        hour: '2-digit',
        minute: '2-digit',
        second: '2-digit',
        fractionalSecondDigits: 3,
      });
      const prefix = prefixMap[event.type] || '   ';
      lines.push(`[${time}] ${prefix}${event.data}`);
    }

    lines.push('');
    lines.push('═══════════════════════════════════════════════════');
    lines.push('  Generated by SMTP Tester (https://smtp-tester.tyk.app)');
    lines.push('═══════════════════════════════════════════════════');

    return lines.join('\n');
  },

  /**
   * Transcript export — RFC 5321 style session transcript.
   */
  toTranscript(events, host) {
    const lines = [
      `; SMTP session transcript`,
      `; Host: ${host}`,
      `; Date: ${new Date().toISOString()}`,
      `; Tool: SMTP Tester (https://smtp-tester.tyk.app)`,
      `;`,
    ];

    for (const event of events) {
      switch (event.type) {
        case 'received':
          // Server lines: prefix each line with "S: "
          event.data.split('\n').forEach(line => {
            lines.push(`S: ${line}`);
          });
          break;
        case 'sent':
          lines.push(`C: ${event.data}`);
          break;
        case 'tls':
          lines.push(`; [TLS] ${event.data}`);
          break;
        case 'info':
          lines.push(`; [INFO] ${event.data}`);
          break;
        case 'error':
          lines.push(`; [ERROR] ${event.data}`);
          break;
        case 'complete':
          lines.push(`; [DONE] ${event.data}`);
          break;
      }
    }

    return lines.join('\n');
  },

  /**
   * Trigger a file download.
   */
  download(content, filename, mimeType) {
    const blob = new Blob([content], { type: mimeType });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = filename;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  },
};
